package other.folder02;

public class p03_operator {
	// 001 Toán tự số học + - * / %
	public static void main(String[] args) {
		int numberOne   = 20;
		int numberTwo   = 3;
		int result;
		
		//+
		result = numberOne + numberTwo;
		System.out.println(numberOne + "+" + numberTwo + "=" + result);
		
		//-
		result = numberOne - numberTwo;
		System.out.println(numberOne + "-" + numberTwo + "=" + result );
		
		result = numberOne * numberTwo;
		System.out.println(numberOne + "*" + numberTwo + "=" + result );
		
		result = numberOne / numberTwo;
		System.out.println(numberOne + "/" + numberTwo + "=" + result );
		
		result = numberOne % numberTwo;
		System.out.println(numberOne + "%" + numberTwo + "=" + result );
		
		
		
		
	}
		

}
